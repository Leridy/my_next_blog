export const generatePoliteWords = () => {
  const returnWords = [...politeWordsTemplate];
  const DateNow = new Date();
  const hour = DateNow.getHours();
  if (hour < 12) {
    returnWords.push('早上好');
  } else if (hour < 18) {
    returnWords.push('下午好');
  } else {
    returnWords.push('晚上好');
  }

  return returnWords;
};

